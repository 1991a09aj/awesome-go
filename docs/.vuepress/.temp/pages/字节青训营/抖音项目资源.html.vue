<h3 id="存储分发怎么做" tabindex="-1"><a class="header-anchor" href="#存储分发怎么做" aria-hidden="true">#</a> 存储分发怎么做</h3>
<p><strong>一种需求，两个方向：</strong> 推，和拉</p>
<ul>
<li>在 Web 方向上的【推】：去数据库，主动查找</li>
<li>在 Web 方向上【拉】：存数据库，交给数据库操作</li>
</ul>
<p>【关注UP主的视频列表】和【我的粉丝列表】</p>
<p><strong>⚠️ IO效率 是重点</strong></p>
<h3 id="怎么通知-粉丝-关注的-up-主的视频列表" tabindex="-1"><a class="header-anchor" href="#怎么通知-粉丝-关注的-up-主的视频列表" aria-hidden="true">#</a> 怎么通知 粉丝 关注的 UP 主的视频列表</h3>
<p>在存储方向上【推】：UP 主有一个粉丝列表，粉丝每次读取的时候，不是从 UP 那边来的，而是从自己的视频池子里拿的，UP 每一次更新，都有会视频大量写入大量粉丝的视频池，<strong>读写IO比 = 1 : 99</strong></p>
<p>在存储方向上【拉】： UP主仍然有个粉丝列表，但是，每一次新视频的推出，仅仅只会通知粉丝们去拉取视频到自己的视频池子里。读写IO比是：99 : 1</p>
<p>暂时业界内还没方法平衡 99 和 1，目前业界解决方案是 更新 自己的数据库</p>
<p>视频有先后，所有，为了表达先后，MySQL 使用 上锁 + 自增ID ，每一次读写，都有可能碰到上锁，数量大了，分库分表怎么办？（粉丝列表和关注列表）视频评论总该存吧？</p>
<p>非关系型 + 表格存储 偏向 无结构 -&gt; MySQL （用户隐私信息）+ Mongo（大量视频相关的元数据）+ Redis （视频相关热点视频，热门视频下的点赞信息，用户方面 JWT Token 授权信息） + 文件服务器（视频本身） -&gt; 对象存储服务</p>
<blockquote>
<p>元数据：文件系统内鼠标右键能看到的【属性】，便是一种元数据</p>
<p>认证和授权（鉴权）：两种不一样的服务，认证在于第一次的使用，包括第一次登录，主要传递 账户 和 摘要算法 Hash 后的 密码；授权，在于认证的后续使用，便捷地去做判断，</p>
<p>JWT Token：认证方式之一，简单理解为，一个 string 走天下。</p>
</blockquote>
