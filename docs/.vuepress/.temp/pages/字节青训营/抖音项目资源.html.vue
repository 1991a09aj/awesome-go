<h2 id="抖音推送机制" tabindex="-1"><a class="header-anchor" href="#抖音推送机制" aria-hidden="true">#</a> 抖音推送机制</h2>
<p>抖音一共有至少不少于三个递进式的流量池第一个流量池，针对自己的粉丝或者同城抖友，平台将审核通过的视频率先推给你的 500 个粉丝，并且是活跃粉丝，如果存在很久不登陆的粉丝，那么会用和你粉丝相似的人来代替在这一个环节，一般都是机审，机器会为你的视频打分，从整体的画质、时长、剪辑等等给你视频一个最初步的打分，这个环节会由评分来决定你是否能获得足额的 500 播放量。</p>
<p>第二个流量池，针对第一波推送后给你视频良好反馈的粉丝的相近人群。用通俗易懂的话来说，就是第一波的 500 人，比如说有 10 人看完了你的视频，3 个人给你点赞，1 个人给你评论，那么这 14 个人就是给你视频带来良好反馈的人，所以第二个流量池就会推送给类似这 14 个人的人群，他们有着很接近的兴趣，愿意在某一类视频上停留，也愿意点赞和转发某一类视频。第二个流量池的人数上限可以到 10W 播放，但是仍然会有机审，并且有人工审核抽查。如果发现有违规或者举报人数过多，仍然会停止推送、删除视频或者封号。第三个流量池，针对的就不再是仅仅对你视频感兴趣的或者和你粉丝接近的人群了，这也就是说为什么抖音不是一个标准意义的“千人千面”的 ugc 了。在这个流量池，你的视频可以达到百万播放，但是面临的就是“大众评审”了。</p>
