<h2 id="什么是分布式系统" tabindex="-1"><a class="header-anchor" href="#什么是分布式系统" aria-hidden="true">#</a> 什么是分布式系统</h2>
<p>分布式操作系统是一组异构计算机和处理器通过网络联接在一起。</p>
<p>分布式系统的目标是为文件系统、名字空间、时间、安全和资源访问提供一个共同的、一致的全局视图，所有的成员都有许多限制和要求，因此分布式系统通常也称为<strong>紧耦合系统</strong>。</p>
<p><strong>那么引出我们想要的概念：</strong></p>
<p>分布式系统（distributed system）是指由多台计算机协同工作的系统。这些计算机可能位于同一地点，也可能分布在不同地点，通常是通过网络连接在一起的。分布式系统与传统的单机系统最大的区别在于，它是由多个计算机协同工作的，这些计算机之间可以互相通信和协调。</p>
<p>分布式系统具有许多优点，包括可扩展性、冗余和容错性。它们可以应用于各种不同的领域，如云计算、大数据处理、分布式数据库、分布式存储等。</p>
